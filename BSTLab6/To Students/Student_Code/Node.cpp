#include "Node.h"

using namespace std;

Node::Node(int initial_data)
{
	data = initial_data;
	right = NULL;
	left = NULL;
}

Node::Node() {
	right = NULL;
	left = NULL;
}

Node::~Node()
{
	right = NULL;
	left = NULL;
}



int Node::getData(){
	return data;
}

NodeInterface* Node::getLeftChild(){
	return left;
}

NodeInterface* Node::getRightChild(){
	return right;
}
